    [Fact]
    public void Should_ResolveOrdinalsOnceAndGuardNulls_When_RowHasNullableAndNonNullableColumns()
    {
        var outcome = GeneratorHarness.Run(CanonicalSource);

        var source = Assert.Single(outcome.GeneratedSources).SourceText.ToString();
        Assert.Contains("""GetOrdinal("Id")""", source);
        Assert.Contains("""GetOrdinal("Name")""", source);
        Assert.Contains("""GetOrdinal("Price")""", source);
        Assert.Contains("default(decimal?)", source);
        Assert.Contains("InvalidOperationException", source);
    }

