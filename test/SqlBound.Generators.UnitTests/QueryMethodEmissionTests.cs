    private const string Prelude = """
        using System.Collections.Generic;
        using System.Data.Common;
        using System.Threading;
        using System.Threading.Tasks;
        using SqlBound;

        namespace App;

        public sealed record Item(int Id, string Name, decimal? Price);

        """;

    private const string CanonicalSource = Prelude + """
        public static partial class ItemQueries
        {
            [SqlQuery("SELECT id, name, price FROM items WHERE category = @category")]
            public static partial Task<IReadOnlyList<Item>> GetItemsAsync(
                DbConnection connection,
                DbTransaction? transaction,
                string category,
                CancellationToken cancellationToken = default);
        }
        """;

    private const string SingleRowSource = Prelude + """
        public static partial class ItemQueries
        {
            [SqlQuery("SELECT id, name, price FROM items WHERE id = @id")]
            public static partial Task<Item> GetByIdAsync(
                DbConnection connection, int id, CancellationToken cancellationToken = default);
        }
        """;

    private const string OptionalRowSource = Prelude + """
        public static partial class ItemQueries
        {
            [SqlQuery("SELECT id, name, price FROM items WHERE id = @id")]
            public static partial Task<Item?> FindByIdAsync(
                DbConnection connection, int id, CancellationToken cancellationToken = default);
        }
        """;

    [Fact]
    public void Should_EmitStrictSingleReadImplementation_When_ReturnTypeIsTaskOfRow()
    {
        var outcome = GeneratorHarness.Run(SingleRowSource);

        AssertCompilesClean(outcome);
        var generated = Assert.Single(outcome.GeneratedSources).SourceText.ToString();
        Assert.Contains("no rows but exactly one was expected", generated);
        Assert.Contains("more than one row", generated);
    }

